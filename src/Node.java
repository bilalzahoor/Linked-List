
public class Node{
		private int address;
		private int data;
		private int next;
		void putAddress(int a){
			address=a;
			
		}
		int getAddress(){
			return address;
			
		}
		void putData(int a){
			data=a;	
		}
		int getData(){
			return data;
		}
		void putNext(int a){
			next=a;
			
		}
		int getNext(){
			return next;
			
		}
	}
